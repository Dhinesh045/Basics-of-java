package constructor;

public class GetterSetter {
    private int id;

    public int getid(){
        return id;
    }

    public void setid(int id){
        this.id=id;
    }


    private String name;

    public String getname(){
        return name;
    }

    public void setname(String name){
        this.name=name;
    }


    private float salary;

    public float getsalary(){
        return salary;

    }

    public void setsalary(float salary){

        this.salary=salary;
    }



}